import turtle

turtle.forward(0)
turtle.shape('turtle')
turtle.color('green','yellow')
turtle.left (180)
turtle.penup ()
turtle.forward (300)
turtle.pendown()
turtle.left(180)

for i in range (4):
    turtle.forward(100)
    turtle.left(90)

turtle.penup()
turtle.forward (500)
turtle.pendown()

for x in range (4):
    turtle.forward (90)
    turtle.left (120)

turtle.exitonclick()























































































































































































